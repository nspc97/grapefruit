    public function store(BookingRequest $request, Trip $trip)
    {
        $user = Auth::user();
        $quantity = $request->input('quantity');

        $booking = new Booking([
            'quantity' => $quantity,
            'total_price' => $quantity * $trip->price,
        ]);

        $booking->user()->associate($user);
        $booking->trip()->associate($trip);
        $booking->save();

        return response()->json($booking, Response::HTTP_CREATED);
    }

